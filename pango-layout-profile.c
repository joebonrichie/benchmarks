static void
measure_strings (GtkWidget *widget)
{
	PangoLayout *layout;
	int i, j;
	GTimer *timer;
	double elapsed;

	timer = g_timer_new ();

	for (i = 0; i < NUM_ITERS; i++)
		for (j = 0; j < G_N_ELEMENTS (strings); j++) {
			PangoRectangle ink_rect, logical_rect;

			layout = gtk_widget_create_pango_layout (widget, strings[j]);
			pango_layout_get_extents (layout, &ink_rect, &logical_rect);

			g_object_unref (layout);
		}

	elapsed = g_timer_elapsed (timer, NULL);
	g_timer_destroy (timer);

	printf ("Total time: %8.6f sec\n", elapsed);
}

static void
profile_pango_layout (void)
{
	GtkWidget *window;

	window = gtk_window_new (GTK_WINDOW_TOPLEVEL);
	gtk_widget_show_now (window);

	measure_strings (window);
	gtk_widget_destroy (window);
}

int
main (int argc, char **argv)
{
	gtk_init (&argc, &argv);

	/* This function will cause some text to be printed in the dialog.  That will make
	 * Pango load its first fonts and generally initialize itself.  The real benchmark
	 * is in profile_pango_layout().
	 */
	dummy_dialog ();

	profile_pango_layout ();
	return 0;
}
